async function burnAndCheckSupply() {
    try {
      const initialSupply = await contract.methods.totalSupply().call();
      console.log('Initial Total Supply:', initialSupply);
      // Estimate gas
      const gasEstimate = await contract.methods.burn(burnAmount).estimateGas({ from: account });
  
      // Call the burn function
      await contract.methods.burn(burnAmount).send({ from: account, gas: gasEstimate });
      console.log(`Burned ${burnAmount} tokens.`);
  
      // Check totalSupply again
      const totalSupply = await contract.methods.totalSupply().call();
      console.log('Updated Total Supply:', totalSupply);
    } catch (err) {
      console.error('Error:', err);
    }
  }

  burnAndCheckSupply();
